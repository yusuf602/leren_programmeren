# example:
def increment(nr: float) -> float:
  return nr+1 

def decrement(nr):
  return nr-1

def add(nr1, nr2):
  return nr1 + nr2 

def subtract(nr1, nr2):
  return nr1-nr2

def multiply(nr1, nr2):
  return  nr1*nr2

def divide(nr1, nr2):
  if nr2 == 0:
    return None
  else: 
    return nr1/ nr2
